#!/usr/bin/env python3
"""Plan safe, object-by-object R2 queue transitions for one scoring snapshot.

The scoring workflow writes the exact ``incoming/`` keys it downloaded to a manifest.
Only those keys may be moved afterward, which prevents a submission arriving mid-run from
being archived without scoring. Status files written by :mod:`benchmark.backend_score`
decide the destination:

* FINISHED -> processed/<relative key>
* FAILED and retryable=false -> failed/<relative key>
* missing, malformed, RUNNING, or retryable failure -> leave in incoming/

The CLI emits tab-separated source/destination/status triples for the workflow. R2 keys are
generated by the intake Worker and therefore contain no tabs or newlines.
"""
from __future__ import annotations

import argparse
import json
from dataclasses import dataclass
from pathlib import Path, PurePosixPath

INCOMING_PREFIX = "incoming/"


@dataclass(frozen=True)
class Transition:
    source: str
    destination: str
    status_path: Path
    status_destination: str | None = None


def _relative_key(key: str) -> PurePosixPath:
    if not key.startswith(INCOMING_PREFIX):
        raise ValueError(f"queue key is outside {INCOMING_PREFIX}: {key!r}")
    relative = PurePosixPath(key.removeprefix(INCOMING_PREFIX))
    if (relative.is_absolute() or not relative.parts
            or any(part in ("", ".", "..") for part in relative.parts)):
        raise ValueError(f"unsafe queue key: {key!r}")
    return relative


def status_path(incoming_dir: Path, key: str) -> Path:
    relative = _relative_key(key)
    filename = relative.name
    stem = filename[:-5] if filename.endswith(".json") else filename
    return incoming_dir.joinpath(*relative.parent.parts, f"{stem}.status.json")


def plan_transition(key: str, incoming_dir: Path) -> Transition | None:
    """Return the durable transition for a snapshotted key, or ``None`` to retry later."""
    relative = _relative_key(key)
    sp = status_path(incoming_dir, key)
    try:
        status = json.loads(sp.read_text(encoding="utf-8"))
    except (FileNotFoundError, OSError, json.JSONDecodeError):
        return None

    if status.get("status") == "FINISHED":
        return Transition(key, f"processed/{relative.as_posix()}", sp)
    if status.get("status") == "FAILED" and status.get("retryable") is False:
        status_dest = f"failed-status/{relative.with_suffix('').as_posix()}.status.json"
        return Transition(key, f"failed/{relative.as_posix()}", sp, status_dest)
    return None


def plan_manifest(manifest: Path, incoming_dir: Path) -> list[Transition]:
    transitions = []
    for line in manifest.read_text(encoding="utf-8").splitlines():
        key = line.strip()
        if key:
            transition = plan_transition(key, incoming_dir)
            if transition is not None:
                transitions.append(transition)
    return transitions


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Plan exact R2 moves for a snapshotted scoring batch")
    parser.add_argument("--manifest", type=Path, required=True)
    parser.add_argument("--incoming-dir", type=Path, required=True)
    args = parser.parse_args()

    for transition in plan_manifest(args.manifest, args.incoming_dir):
        print("\t".join((
            transition.source,
            transition.destination,
            str(transition.status_path),
            transition.status_destination or "-",
        )))


if __name__ == "__main__":
    main()
